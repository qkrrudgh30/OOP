package academy.codechosun;

public class Casting {
    public static void main(String[] args) {
        // Implicit Type Casting
        int i = 10;
        long l = i;
        float f = l;
        double d = f;

        // Explicit Type Casting
        double d2 = 3.141592;
        float f2 = (float)d2;
        long l2 = (long)f2;
        int i2 = (int)l2;

    }
}
