package academy.codechosun;

public class FloatTypeLiteral {
    public static void main(String[] args) {
        float f_num = 3.14f;
        double d_num = 3.141592;

        System.out.printf("f_num: %f%s", f_num, System.lineSeparator());
        System.out.printf("d_num: %f%s", d_num, System.lineSeparator());

    }
}